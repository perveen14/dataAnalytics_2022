#Simple program to show a for loop

def main():
    print("Welcome to my program!")
    
    #Everything indented after the for statement is the body of the loop
    for i in range(1,11):
        print(i, end = ".")
        print("I Love Python!")

    #always back out of the loop when you are done with it
    print("Thank you for using my program!")

    #Display "Buy" 5 times
    for i in range(5):
        print("Buy")
    

#call or invoke the main function 
main()

        
        

    
